public class DocumentTemplateException extends Exception
{

    public DocumentTemplateException()
    {
        super();
    }

    public DocumentTemplateException(String s)
    {
        super(s);
    }

    public DocumentTemplateException(String s, Throwable throwable)
    {
        super(s, throwable);
    }

    public DocumentTemplateException(Throwable throwable)
    {
        super(throwable);
    }
}
